		public function getCategoriaCurso($id)
		{
		
			$query = "SELECT idCategoria, nombreCategoria, nivel
			FROM categoria_curso
					where idCategoria = '" . mysqli_real_escape_string($this->dbLink, $id) . "'
					";
			$arreglo = array();
			$resultado = mysqli_query($this->dbLink, $query);
			if ($resultado && mysqli_num_rows($resultado) > 0) {
				while ($row_inf = mysqli_fetch_assoc($resultado)){
					$arreglo[] = $row_inf;
				}
			}
			return $arreglo;
		}
		
		public function getCategoriasCurso($pagina,$tamano)
		{
			$inicial = (($pagina) * $tamano);
			$query = "SELECT  idCategoria, nombreCategoria, nivel
			FROM categoria_curso
			LIMIT $inicial, $tamano
			";
			$arreglo = array();
			$resultado = mysqli_query($this->dbLink, $query);
			if ($resultado && mysqli_num_rows($resultado) > 0) {
